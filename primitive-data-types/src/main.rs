fn main() {
    // Integers sections
    let x: i32 = -50;
    let y: u64 = 100;

    println!("Signed Integer: {}", x);
    println!("UnSigned Integer: {}", y);

    // Floats
    // f32, f64
    let pi: f64 = 3.14;
    println!("Value of pi: {}", pi);

    // Boolean: true/false
    let is_enabled: bool = true;
    println!("is it enabled ? {}", is_enabled);

    // Character type - char (unicode character)
    let letter: char = 'a';
    println!("First letter of alphabet: {}", letter);
}
